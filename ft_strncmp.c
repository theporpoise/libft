#include <libft.h>

int	ft_strncmp(char *s1, char *s2, size_t n)
{
	while (*s1 && *s2 && (*s1 == *s2) && (n > 0))
	{
		s1++;
		s2++;
		n--;
	}
	if (*(s1 - 1) == *(s2 - 1) && n == 0)
		return (0);
	else if (*s1 == *s2)
		return (0);
	else if (*s1 == '\0' && *s2 != '\0')
		return (-1);
	else if (*s2 == '\0' && *s1 != '\0')
		return (1);
	return (*(unsigned char*)s1 - *(unsigned char*)s2);
}
