int	ft_strncmp(char *s1, char *s2, size_t n)
{
	int i;
	int dif;

	i = 0;
	dif = 0;
	while (s1[i] != '\0' && (i < n))
	{
		dif = s1[i] - s2[i];
		if (s2[i] == '\0' || (dif != 0))
			return (dif);
		i++;
	}
	return (-(s2[i]));
}
