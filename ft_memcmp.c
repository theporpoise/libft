int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	int i;
	int dif;

	i = 0;
	dif = 0;
	while (i < n)
	{
		dif = s1[i] - s2[i];
		if (dif != 0)
			return (dif);
		i++;
	}
	return (0);
}
